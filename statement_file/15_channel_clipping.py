def final_target(r):
    if r['channel_Name'] in excluded_channels:
        return r['Actual_Duration']
    if r['uniqueviewercount'] < 5:
        return 0
    return min(r['Actual_Duration'], r['P95'], r['absolute cap sec'])

cap_df['Target_Duration'] = cap_df.apply(final_target, axis=1)
cap_df['Capped Value'] = cap_df['Target_Duration']


cap_df['Cut_Required'] = (
    cap_df['Actual_Duration'] - cap_df['Target_Duration']
)

cap_df['Rule_Label'] = np.where(
    cap_df['channel_Name'].isin(excluded_channels),
    'Excluded channel – No cap',
    np.where(
        cap_df['uniqueviewercount'] < 5,
        'Low sample (<5) → Zero',
        'P95 + Absolute cap'
    )
)

# ============================================================
# SPLIT 30-MIN + STRICT MIDNIGHT
# ============================================================
split_rows = []

for _, row in raw_df.iterrows():

    start = float(row['start_time_secs'])
    remaining = float(row['duration_seconds'])

    while remaining > 0:

        seconds_to_midnight = 86400 - (start % 86400)
        seconds_to_30 = 1800 - (start % 1800)

        chunk = min(remaining, seconds_to_midnight, seconds_to_30)

        new_row = row.copy()
        new_row['start_sec'] = start
        new_row['end_sec'] = start + chunk
        new_row['Original_Split_Duration'] = chunk
        new_row['duration_seconds'] = chunk

        split_rows.append(new_row)

        start += chunk
        remaining -= chunk

split_df = pd.DataFrame(split_rows).reset_index(drop=True)




