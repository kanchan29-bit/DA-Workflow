# ============================================================
# ASSIGN DAYPART
# ============================================================

start_mod = split_df['start_sec'] % 86400

# shift 00:00–02:00 to 86400+ range (because your day starts at 02:00)
start_mod = np.where(start_mod < 7200,
                     start_mod + 86400,
                     start_mod)

split_df['start_sec_mod'] = start_mod

def assign_daypart(sec):
    match = daypart_df[
        (daypart_df['Start Time'] <= sec) &
        (daypart_df['End Time'] > sec)
    ]
    if not match.empty:
        r = match.iloc[0]
        return pd.Series([r['DP ID'], r['Priorities'], r['priorties_ID']])
    return pd.Series([None, None, None])

split_df[['DP ID','Priorities','priorties_ID']] = (
    split_df['start_sec_mod'].apply(assign_daypart)
)

# ============================================================
