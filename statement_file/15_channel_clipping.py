# ============================================================
# TRIMMING RULE WITH CASE IDENTIFICATION
# APPLY ONLY IF clipping require == TRUE
# ============================================================

for (member, channel), group in split_df.groupby(['Full_Member_ID','channelid']):

    cut_needed = group['Cut_Required'].iloc[0]
    clipping_flag = group['clipping require'].iloc[0]

    # ---- Skip if no clipping required ----
    if (pd.isna(cut_needed) or cut_needed <= 0) or (clipping_flag != True):
        continue

    group_sorted = group.sort_values(
        ['priorties_ID','start_sec'],
        ascending=[False, False]
    )

    # ================= CASE 4 =================
    if len(group_sorted) == 1:

        idx = group_sorted.index[0]

        original_start = split_df.at[idx, 'start_sec']
        original_duration = split_df.at[idx, 'duration_seconds']

        cut_amt = min(original_duration, cut_needed)

        shift = cut_amt / 2

        split_df.at[idx, 'start_sec'] = original_start + shift
        split_df.at[idx, 'duration_seconds'] = original_duration - cut_amt
        split_df.at[idx, 'end_sec'] = (
            split_df.at[idx, 'start_sec'] +
            split_df.at[idx, 'duration_seconds']
        )

        split_df.loc[group.index, 'Case_Number'] = 4
        continue

    # ================= MULTI-BLOCK CASES =================
    cut_positions = []

    for idx in group_sorted.index:

        if cut_needed <= 0:
            break

        available = split_df.at[idx, 'duration_seconds']
        cut_amt = min(available, cut_needed)

        if cut_amt > 0:
            cut_positions.append(idx)

        split_df.at[idx, 'duration_seconds'] -= cut_amt
        split_df.at[idx, 'end_sec'] = (
            split_df.at[idx, 'start_sec'] +
            split_df.at[idx, 'duration_seconds']
        )

        cut_needed -= cut_amt

    # ---- Identify Case Type ----
    if len(cut_positions) == 1:
        cut_idx = cut_positions[0]
        position_rank = group_sorted.index.get_loc(cut_idx)

        if position_rank == 0:
            case = 2   # last block
        elif position_rank == len(group_sorted) - 1:
            case = 1   # first block
        else:
            case = 3   # middle block
    else:
        case = 3

    split_df.loc[group.index, 'Case_Number'] = case

